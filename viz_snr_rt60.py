def create_twined_error_plot():
    x_top_label = "RT60 @SNR=20dB (s)"
    x_bottom_label = "SNR @RT60=0.4s (dB)"
    y_label = "Error ()"
    fig, ax1 = plt.subplots()
    ax2 = ax1.twiny()
    ax1.set_xlabel(x_top_label, weight="bold")
    ax2.set_xlabel(x_bottom_label, weight="bold")
    ax1.set_ylabel(y_label)

    ax1.xaxis.label.set_color(COLORS[0])
    ax2.xaxis.label.set_color(COLORS[1])

    return fig, ax1, ax2


def plot_error_curve(ax, labels, values, color="red", label=None, linestyle="-"):
    labels = np.array(labels)
    values = np.array(values)

    labels = np.array([l for l in labels])
    sort_idxs = np.argsort(labels)

    labels = labels[sort_idxs]
    values = values[sort_idxs]
    params = np.polyfit(labels, values, 3)
    p = np.poly1d(params)
    trend = p(labels)

    # ax.plot(labels, values, color=color, alpha=0.5)
    ax.plot(labels, trend, color=color, label=label, linestyle=linestyle)

    return ax


